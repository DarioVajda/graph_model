import os
import torch
import numpy as np
from datasets import load_dataset
from transformers import (
    AutoModelForCausalLM,
    AutoTokenizer,
    TrainingArguments,
    Trainer,
    DataCollatorForSeq2Seq
)
from peft import LoraConfig, get_peft_model, prepare_model_for_kbit_training

def set_wandb_project(project_name="GraphLLM"):
    import os
    os.environ["WANDB_PROJECT"] = project_name
    os.environ["WANDB_WATCH"] = "false"

def preprocess_logits_for_metrics(logits, labels):
    """
    Evaluates argmax on the GPU so we don't accumulate massive logit 
    tensors on the CPU across the entire validation loop (prevents CPU OOM).
    """
    # Some models output a tuple (logits, past_key_values)
    if isinstance(logits, tuple):
        logits = logits[0]
        
    preds = logits.argmax(dim=-1)
    return preds

def compute_metrics(eval_preds):
    preds, labels = eval_preds
    
    # Causal LM shifting: logits at step t predict the token at step t+1.
    # Therefore, we shift predictions left and labels right to align them.
    preds = preds[:, :-1]
    labels = labels[:, 1:]
    
    y_true, y_pred = [], []
    
    for i in range(len(labels)):
        # Identify where labels are not masked
        valid = labels[i] != -100
        if not np.any(valid): 
            continue
        
        # Extract the sequence of true tokens and predicted tokens
        y_true.append("-".join(map(str, labels[i][valid].tolist())))
        y_pred.append("-".join(map(str, preds[i][valid].tolist())))

    # Compute Exact Match accuracy
    accuracy = np.mean([t == p for t, p in zip(y_true, y_pred)])

    # Compute F1 score (using your exact logic)
    if len(y_true) == 0:
        return {"em_accuracy": 0.0, "em_f1": 0.0}

    unique_classes = list(set(y_true))
    majority_class = max(set(y_true), key=y_true.count)
    
    tp = { cl: 0 for cl in unique_classes }
    fp = { cl: 0 for cl in unique_classes }
    fn = { cl: 0 for cl in unique_classes }
    
    for true, pred in zip(y_true, y_pred):
        if true == pred:
            tp[true] += 1
        else:
            if pred in unique_classes:
                fp[pred] += 1
            else:
                # Handle model hallucinations by predicting majority class
                fp[majority_class] += 1
            fn[true] += 1
            
    f1_scores = {
        cl: (2 * tp[cl]) / (2 * tp[cl] + fp[cl] + fn[cl]) if (2 * tp[cl] + fp[cl] + fn[cl]) > 0 else 0.0
        for cl in unique_classes
    }
    
    macro_f1 = np.mean(list(f1_scores.values()))

    return {
        "em_accuracy": float(accuracy),
        "em_f1": float(macro_f1),
    }

def main():
    set_wandb_project("GraphLLM")

    # --- Configuration ---
    model_id = "meta-llama/Llama-3.2-1B"
    # Adjust this to match the actual folder generated by your script
    dataset_path = "./src/experiments/knowledge_graph_qa/text_datasets/dataset_40-60" 
    output_dir = "./trained_models/llama-3.2-1B-kg-qa-lora"
    
    # --- 1. Load Tokenizer & Model ---
    print("Loading tokenizer and model...")
    tokenizer = AutoTokenizer.from_pretrained(model_id)
    
    # We load the model natively in bfloat16 and utilize Flash Attention 2 for speed
    model = AutoModelForCausalLM.from_pretrained(
        model_id,
        torch_dtype=torch.bfloat16,
        attn_implementation="sdpa",
        device_map={"": torch.cuda.current_device()} # Required for multi-GPU with DDP
    )
    
    # --- 2. Apply LoRA ---
    # Targeting all linear layers is the standard best practice for Llama architectures
    lora_config = LoraConfig(
        r=32,
        lora_alpha=64,
        target_modules=["q_proj", "k_proj", "v_proj", "o_proj", "gate_proj", "up_proj", "down_proj"],
        lora_dropout=0.05,
        bias="none",
        task_type="CAUSAL_LM"
    )
    model = get_peft_model(model, lora_config)
    
    if torch.distributed.get_rank() == 0:
        model.print_trainable_parameters()
    
    # --- 3. Load Dataset ---
    print("Loading dataset...")
    data_files = {
        "train": os.path.join(dataset_path, "train_dataset.jsonl"),
        "validation": os.path.join(dataset_path, "val_dataset.jsonl")
    }
    dataset = load_dataset("json", data_files=data_files)
    
    # Your script already pre-tokenized and formatted input_ids and labels. 
    # DataCollatorForSeq2Seq will handle dynamic padding for the batches.
    tokenizer.pad_token = tokenizer.eos_token
    data_collator = DataCollatorForSeq2Seq(
        tokenizer,
        model=model,
        padding="longest",
        label_pad_token_id=-100
    )
    
    # --- 4. Training Arguments ---
    training_args = TrainingArguments(
        output_dir=output_dir,
        eval_strategy="steps",
        save_strategy="steps",
        eval_steps=80,
        save_steps=80,
        learning_rate=3e-4,
        per_device_train_batch_size=4, 
        per_device_eval_batch_size=8,
        gradient_accumulation_steps=2, # Effective batch size = 4(GPUs) * 4(bs) * 2(grad_acc) = 32
        num_train_epochs=3,
        weight_decay=0.01,
        bf16=True,                     # Strict bfloat16 for A100s
        logging_steps=10,
        gradient_checkpointing=True,
        gradient_checkpointing_kwargs={"use_reentrant": False},
        ddp_find_unused_parameters=False, # Essential for standard DDP with LoRA
        dataloader_num_workers=4,
        report_to="wandb",               # Set to "wandb" if you use Weights & Biases
        run_name="text_kg_qa_lora"
    )
    
    # --- 5. Trainer Initialization ---
    trainer = Trainer(
        model=model,
        args=training_args,
        train_dataset=dataset["train"],
        eval_dataset=dataset["validation"],
        data_collator=data_collator,
        compute_metrics=compute_metrics,
        preprocess_logits_for_metrics=preprocess_logits_for_metrics
    )
    
    # --- 6. Train ---
    print("Starting training...")
    trainer.train()
    
    # --- 7. Save Final Model ---
    if torch.distributed.get_rank() == 0:
        print(f"Saving final adapter to {output_dir}/final")
        trainer.save_model(os.path.join(output_dir, "final"))
        tokenizer.save_pretrained(os.path.join(output_dir, "final"))

if __name__ == "__main__":
    main()
